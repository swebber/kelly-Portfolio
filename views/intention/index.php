            <h4>Intention</h4>
            <p><i>Intention</i> exists as a concept for a quarterly magazine. <i>Intention</i> is not based on the belief that there is one correct way to live life, so is not intended to function as a rule book or a one-size-fits-all model. <i>Intention</i> is neither a doctrine nor a formulaic how-to guide; instead it aims to start conversations and encourage people, families, and organizations to name principles on which to base decisions, leading to a life lived with <i>Intention</i>. <i>Intention</i> believes that each person, family, and organization will require a unique set of guiding principles by which to live and operate. <i>Intention</i> provides uncommon resources and ideas that stray from the typical lifestyles portrayed within Western society. </p>
